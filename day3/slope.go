func FormatInput(input [][]string) [][]string {
	n := make([][]string, len(input))
	for i, v := range input {
		n[i] = strings.Split(v[0], "")	
	}
	return n
}

func CalculateSlope(input [][]string, xSlope int, ySlope int) int {
	var count int
	x := 0
	for y := 0; y+1 < len(input); {
		x += xSlope
		y += ySlope
		l := len(input[0]) - 1
		if x > l {
			x = x - l - 1
		}
		if string(input[y][x]) == "#" {
			count++
		}
	}
	return count
}